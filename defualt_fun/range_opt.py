"""
Range Function:
    -> The range function is used to generate a sequence of numbers, mainly in loops.
    -> syntax,
        range(start, stop, step)
        -> Start, (Optional) The starting number (defualt is 0).
        -> Stop, (Required) The number where it stops (Not Included).
        -> Step, (Optional) The increment/decrement (defualt is 1)
"""
